package com.ruby.java.ch06;

public class Armor { //메인 어디갔누?
	private String name; // 값 안주면 null 스트링
	private int height; // 이건 0 인트

	public String getName() {
		return name;
	}

	public void setName(String value) {
		name = value;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int value) {
		height = value;
	}
}